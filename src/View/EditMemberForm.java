    public void showTableMember(Group group) throws IOException, ClassNotFoundException{
        ChatClient client = new ChatClient();
        list = client.requestListMember(group.getId());
        while(model.getRowCount()!=0){
            model.removeRow(0);
        }
        for(User u:list){
            if(u.getId() == group.getLeaderID()){
                model.addRow(new Object[]{
                    u.getName()+" (Leader)"
                });
            }
            else{
                model.addRow(new Object[]{
                   u.getName()
                });
            }
        }
    }
