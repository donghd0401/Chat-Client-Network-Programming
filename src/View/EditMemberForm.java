    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = tblMember.getSelectedRow();
        int userID = list.get(row).getId();
        try {
            ChatClient client = new ChatClient();
            if(client.requestDeleteMember(userID, this.group.getId())){
                JOptionPane.showMessageDialog(this, "Deleted");
                showTableMember(this.group);
            }
            else{
                JOptionPane.showMessageDialog(this, "Something went wrong");
            }
            client.close();
        } catch (IOException ex) {
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(EditMemberForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String input = JOptionPane.showInputDialog("Enter user ID: ");
        int userID = Integer.parseInt(input);
        Message message = new Message();
        message.setUserID(userID);
        message.setGroupID(this.group.getId());
        message.setContent("Xin chao(Auto-send message)");
        try {
            ChatClient client = new ChatClient();
            if(client.requestSendMessage(message)){
                JOptionPane.showMessageDialog(this, "Added");
                showTableMember(this.group);
            }
            else{
                JOptionPane.showMessageDialog(this, "Something went wrong");
            }
            client.close();
        } catch (IOException | ClassNotFoundException ex) {
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

